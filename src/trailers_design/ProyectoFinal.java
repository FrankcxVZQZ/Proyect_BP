    void ConsDatos(String valor){
        DefaultTableModel modelo= new DefaultTableModel();
       modelo.addColumn("ID");
        modelo.addColumn("Colonia");
        modelo.addColumn("kg");
        modelo.addColumn("Tipo pedido");
        modelo.addColumn("Tipo Pago");
        modelo.addColumn("Total");
        modelo.addColumn("clave");
        modelo.addColumn("No. Cliente");
        modelo.addColumn("direccion");
        tblTrailers.setModel(modelo);
        String sql;
        ResultSet rs;
        Statement st;
        String[] datos = new String [9];
        if(valor.equals(""))
            sql="SELECT * FROM trailersd.servicio;";
        else
            sql="SELECT * FROM trailersd.servicio WHERE id_pedido = "+ valor+";";
        try{
            st=cn.createStatement();
            rs =st.executeQuery(sql);
         while(rs.next()){
             datos[0] = Integer.toString(rs.getInt(1));//pedido
             datos[1] = rs.getString(2);//sercol
             datos[2] = Integer.toString(rs.getInt(3));//kg
             datos[3] = rs.getString(4);//tipo_ped
             datos[4] = rs.getString(5);//tipo-pago
             datos[5] = Double.toString(rs.getDouble(6));//total
             datos[6] =Integer.toString(rs.getInt(7));//clave chofer
             datos[7] =Integer.toString(rs.getInt(8));//No. cliente
             datos[8] =Integer.toString(rs.getInt(9));//direccion
             modelo.addRow(datos);
            if(valor!=("")){
                txtIdPed.setText(datos[0].toString());
                txtServCol.setText(datos[1].toString());
                txtKg.setText(datos[2].toString());
                txtTpPed.setText(datos[3].toString());
                txtTpPago.setText(datos[4].toString());
                txtPagoT.setText(datos[5].toString());
                cmbCveChofer.setSelectedItem(datos[6].toString()); 
                txtNoCliente.setText(datos[7].toString());;
                txtIdDirec.setText(datos[8].toString());
              }
            } 
         tblTrailers.setModel(modelo);
         rs.close();
        }catch(SQLException ex){
            Logger.getLogger(ProyectoFinal.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
    private void LlenaCombo(){
        String sql= "SELECT * FROM trailersd.chofer;";
        ResultSet rs;
        Statement st;
        try{
        st=cn.createStatement();
        rs = st.executeQuery(sql);
        while(rs.next()){
            String ClaveChofer=Integer.toString(rs.getInt(1));
            cmbCveChofer.addItem(ClaveChofer);
        } 
         rs.close();    
        }catch (SQLException ex){
            Logger.getLogger(ProyectoFinal.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
    
