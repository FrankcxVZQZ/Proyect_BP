    private void jMenuEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuEliminarActionPerformed
        // TODO add your handling code here:
        String codigo;
        int fila=tblTrailers.getSelectedRow();
        if(fila>=0){
            codigo=tblTrailers.getValueAt(fila,0).toString();
            try{
             PreparedStatement pst =cn.prepareStatement("DELETE FROM trailersd.servicio WHERE id_pedido ="
                     +codigo+";");
                
            int valor = JOptionPane.showConfirmDialog(null,"¿Estas Seguro de borrar el registro?"
                    ,"Confrimar Eliminacion",JOptionPane.YES_NO_OPTION);
            if(valor==0){
            pst.executeUpdate();
            ConsDatos("");
            }
            }catch (SQLException ex){
            
            Logger.getLogger(ProyectoFinal.class.getName()).log(Level.SEVERE, null,    ex);
          } 
        }
        else{
            JOptionPane.showMessageDialog(null, "No selecciono  ninguna fila");
        }
        limpiarDatos();
    }//GEN-LAST:event_jMenuEliminarActionPerformed

